package ds.io.Graph;

public class VertexInfoD {
	
	private int vertexId;
	private int distance;
	public VertexInfoD(int vertexId, int distance) {
		this.vertexId = vertexId;
		this.distance = distance;
	}
	public int getVertexId() {
		return vertexId;
	}
	public int getDistance() {
		return distance;
	}
	
	

}
